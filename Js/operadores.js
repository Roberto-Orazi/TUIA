/*
Operadores de asignacion
Asigna un valor operando de la izquierda basado en el valor operando de la derecha
*/
x = y // Asignacion
x += y // Asignacion de Suma
x -= y // Asignacion de Resta
x *= y // Asignacion de Multiplicacion
x /= y // Asignacion de Division
x %= y // Asignacion de Resto
x **= y // Asignacion de Exponente
x <<= y // Asignacion de desplazamiento a la izquierda
x >>= y // Asignacion de Desplazamiento a la derecha
x >>>= y // Asignacion Sin signo de desplazamiento a la derecha
x &= y // Asignacion And
x ^= y // Asignacion XOR
x |= y // Asignacion OR



