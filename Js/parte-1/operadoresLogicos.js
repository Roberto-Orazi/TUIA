numeroMayor = 2
numeroMenor = 1
// Operador comparacion, devuelven true o false
numeroMayor == numeroMenor // Igual
numeroMayor != numeroMenor // Distinto
numeroMayor === numeroMenor // Estrictamente igual(valor y tipo)
numeroMayor !== numeroMenor // Estrictamente distinto(valor y tipo)
numeroMayor > numeroMenor // Mayor
numeroMayor < numeroMenor // Menor
numeroMayor >= numeroMenor // Mayor o igual
numeroMayor <= numeroMenor // Menor o igual

// Operadores logicos devuelven tambien true o false
numeroMayor && numeroMenor //AND(&&) las 2 tienen que ser true para devolver true
numeroMayor || numeroMenor //OR(||) con que sea 1 sola true ya devuelve true
!numeroMayor //NOT(!) devuelve el contrario a nuestro valor
